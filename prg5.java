//write a java prg to find transpose of given matrix
public class prg5 {
    public static void main(String[] args) {
        int[][] arr={{7,3,5},{2,4,6}};
    for(int i=0;i<arr[0].length;i++){
        for(int j=0;j<arr.length;j++){
             System.out.print(arr[j][i]+" ");
        }
        System.out.println("");
    }
}
}
