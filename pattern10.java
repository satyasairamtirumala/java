public class pattern10 {
    public static void main(String[] args) {
        for(int i=0 ;i <=3;i++){
            for(int j=0;j<=3;j++){
                if(j==0 || i==3){
                    System.out.print("* ");
                }
                
            }
            System.out.println();
        }
    }
}
